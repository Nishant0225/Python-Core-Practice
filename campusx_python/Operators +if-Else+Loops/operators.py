# Arithmatic Operator
# + , -, * , / , // ,% ,
print("Arithmatic Operator:  + , -, * , / , // , % , ")
print(5+6)
print(5-6)
print(5*6)
print(5/2)
print(5//2)
print(5%2)
print(5**2)

# Relational operator
#  <,>,=,<=,>=,==,!=
print("Relational operator : <,>,=,<=,>=,==,!=")
print(4>5)
print(4<5)
print(4>=4)
print(4<=4)
print(4==4)
print(4!=4)

