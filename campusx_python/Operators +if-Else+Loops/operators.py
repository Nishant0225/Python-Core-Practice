# Bitwise Operators
# are special operators which are operated on binary values
# Bitwise and ,Bitwise or ,Bitwise nor ,Bitwise ex-or ,Bitwise right shift ,Bitwise left shift ,Bitwise not 
print("Bitwise Operators: Bitwise and ,Bitwise or ,Bitwise nor ,Bitwise ex-or ,Bitwise right shift ,Bitwise left shift ,Bitwise not ")

# bitwise and
print(2 & 3)

# bitwise or
print(2 | 3)

# bitwise xor
print(2 ^ 3)

print(~3)

print(4 >> 2)

print(5 << 2)

