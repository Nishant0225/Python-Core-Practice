# ==========================================
# Program to Reverse a List
# Using the reverse() Method
# ==========================================

# Create a list
a = [1, 2, 3, 4, 5, 6, 7, 8]

# Reverse the order of elements in the list
a.reverse()

# Display the reversed list
print(f"The reversed list is: {a}")

# Example:
# Original List: [1, 2, 3, 4, 5, 6, 7, 8]
#
# Output:
# The reversed list is:
# [8, 7, 6, 5, 4, 3, 2, 1]