# ==========================================
# Program to Find the Sum of All Elements
# in a List
# ==========================================

# Create a list of numbers
numbers = [1, 2, 3, 4, 5]

# Initialize the sum variable
total = 0

# Traverse each element in the list
for num in numbers:
    # Add the current element to the total
    total += num

# Display the sum of all elements
print(f"The sum of all elements in the list is: {total}")

# Example:
# List: [1, 2, 3, 4, 5]
#
# Calculation:
# 1 + 2 + 3 + 4 + 5 = 15
#
# Output:
# The sum of all elements in the list is: 15