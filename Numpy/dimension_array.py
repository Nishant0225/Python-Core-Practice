# ==========================================
# Program to Find the Number of Dimensions
# of a NumPy Array
# ==========================================

# Import the NumPy library
import numpy as np

# Create a 2-dimensional NumPy array
a = np.array([
    [1, 2, 3],
    [8, 9, 10]
])

# Display the number of dimensions of the array
# ndim represents the number of dimensions (axes) of the array
print(f"The number of dimensions of the array is: {a.ndim}")

# Output:
# The number of dimensions of the array is: 2